public class Course {

    private int ID;
    private String name;
    private String type;
    private String description;
    private int budget;

    public Course(int ID, String name, String type, String description, int budget) {
        this.ID = ID;
        this.name = name;
        this.type = type;
        this.description = description;
        this.budget = budget;
    }
}