public class Application {

    private int id;
    private String name;
    private String surname;
    private int age;
    private long TCK;
    private String address;

    public Application(int id, String name, String surname, int age, long TCK, String address) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.TCK = TCK;
        this.address = address;
    }
}